#pragma once

#ifndef VS3_CODEFACTORY_CRYPTOGRAPHY_STDINTYPES_H
#define VS3_CODEFACTORY_CRYPTOGRAPHY_STDINTYPES_H

#include <cstdint>
#include <cstddef>
#include <VS3Architecture.h>

// Represents an IEEE-754 single precision floating point type
typedef float f32;

// Represents an IEEE-754 double precision floating point type
typedef double f64;

// Represents a unsigned 8-bit integer
typedef unsigned char u8;

// Represents a signed 8-bit integer
typedef signed char s8;

// Represents a unsigned 16-bit integer
typedef unsigned short u16;

// Represents a signed 16-bit integer
typedef signed short s16;

// Represents a unsigned 32-bit integer
typedef unsigned int u32;

// Represents a signed 32-bit integer
typedef signed int s32;

// Represents a unsigned 64-bit integer
typedef unsigned long long u64;

// Represents a signed 64-bit integer
typedef signed long long s64;

#endif // VS3_CODEFACTORY_CRYPTOGRAPHY_STDINTYPES_H
